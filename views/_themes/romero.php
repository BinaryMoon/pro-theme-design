    <p class="intro">Romero is designed for magazine sites, highlighting full-width featured images that help your main blog posts really stand out. It’s an ideal theme for video game sites, car magazines, and other topics well suited to large, vibrant imagery.</p>

    <h2>Your Design, Everywhere</h2>

    <p>We designed Romero to look good on all devices – from desktop computers and laptops, to smartphones and tablets.</p>

    <h2>Featured Content</h2>

    <p>Romero makes use of Featured Content to add your most important posts to the top of the homepage – including a striking full-width image.</p>

    <h2>Custom Headers and Backgrounds</h2>

    <p>Romero lets you upload your own header image via Appearance → Customize → Header. You can also customize your blog with a background color or image by going to Appearance → Customize → Colors & Backgrounds.</p>

    <h2>Widget Areas</h2>

    <p>Romero uses a series of custom sidebars to keep things flexible. All widget areas stay hidden unless you add widgets to them.</p>

    <ul>
        <li><strong>Footer widget area</strong> – Displays at the bottom of every page on the site.</li>
        <li><strong>Sidebar widget area</strong> – Displays to the left of the homepage, and to the right of single posts and pages. Does not display on archive pages.</li>
        <li><strong>Menu widgets</strong> – Displays in a slide-down panel below the navigation, and includes three widget areas displayed in separate columns. If these widget areas are empty, the button to open the panel does not display.</li>
    </ul>

    <h2>Custom Templates</h2>

    <p>Romero includes two custom page templates, providing additional functionality.</p>

    <ul>
        <li><strong>Contributors</strong> – A list of all of the contributors to your site, sorted based on how many posts each person has published. Great for magazines and other multi-author blogs.</li>
        <li><strong>Full-Width Page</strong> – displays a normal page, but with the sidebars removed so that you can have content that stretches the full width of the site.</li>
    </ul>

