    <p class="intro">Mimbo Pro is a magazine theme for WordPress. Unlike a traditional blog theme it takes your content and formats it in a structured way grouped by category. Using all of the standard WordPress functionality, custom headers, custom backgrounds, post thumbnails, custom navigation and more – Mimbo Pro makes your content look awesome.</p>

    <h2>Featured Content</h2>

    <p>At the top of the homepage is a featured content space. This lets you show off your most important content. By default you Mimbo Pro displays all posts here – however you can restrict the content to specific categories by using the theme control panel – and selecting a featured category.</p>

    <h2>Homepage Carousel</h2>

    <p>Mimbo Pro features a homepage carousel that shows off your featured content. You can edit the carousel settings through the theme options.</p>

    <h2>Admin Panel to Control Homepage Settings</h2>

    <p>The carousel options allow you to change the carousel categories, the scroll speed of the category. 0 = no automatic scrolling, and a number greater than 0 is the amount of time to pause between scrolling. If you want you can also hide the carousel by selecting the hide option in the category selection.</p>

    <h2>Custom Colours</h2>

    <p>Mimbo Pro allows you to edit the theme colours from the theme customiser. This is a powerful feature that gives you the ability to make the theme fit your design and branding as closely as possible.</p>

    <h2>Category Summaries</h2>

    <p>Mimbo Pro allows you to display your categories on the homepage along with the 4 latest posts. We call this section the category summaries. By default all categories are displayed, however you can limit the theme to specific categories through the control panel.</p>

    <h2>Custom Sidebars</h2>

    <p>Mimbo Pro uses a series of custom sidebars to allow you to keep things nice and flexible.</p>

    <ul>
        <li><strong>Sidebar Left</strong> – the default left hand sidebar.</li>
        <li><strong>Sidebar Right</strong> – the right hand sidebar as displayed on the homepage.</li>
        <li><strong>Sidebar Single Post Left</strong> – the left hand sidebar to use on single post pages. This is optional, if you leave it empty then the Sidebar Left will be used.</li>
        <li><strong>Sidebar Single Post Right</strong> – This is another optional sidebar. Leave it empty and it will be hidden. Add widgets and it will be displayed on the single posts pages within the post content.</li>
        <li><strong>Bottom Widgets</strong> – footer widgets. These will be displayed side by side in the footer. It’s recommended that you use 3 widgets since there are three columns, however you can use as many as you like.</li>
    </ul>


    <h2>Custom Widgets</h2>

    <p>Mimbo Pro includes some custom widgets that are unique to the theme.</p>

    <ul>
        <li><strong>Upcoming Posts</strong> – this widgets lists any posts that you have scheduled for posting in the future.</li>
        <li><strong>Related Posts (by category)</strong> – lists other posts that are in the category/ categories that the current post is in.</li>
        <li><strong>Popular Posts</strong> – lists posts ranked by comment count. The more comments a post has, the more popular it must be!</li>
        <li><strong>Author Details</strong> – displays some information about the post author. Only shows on the single post pages and won’t be visible on archive pages, or the homepage.</li>
    </ul>

    <h2>Custom Templates</h2>

    <p>Mimbo Pro includes some custom page templates. These give you some additional functionality that you wouldn’t get with a normal theme.</p>

    <ul>
        <li><strong>Archives</strong> – A chronological list of all posts published on your blog.</li>
        <li><strong>Full Width Page</strong> – A normal WordPress page, only it has the sidebars removed so that you can have content that stretches the full width of the site.</li>
        <li><strong>Image Gallery</strong> – A page that displays all the image attachments on the site and links to their respective pages – nice for visual sites.</li>
        <li><strong>Sitemap</strong> – A complete list of all website posts and pages.</li>
    </ul>

