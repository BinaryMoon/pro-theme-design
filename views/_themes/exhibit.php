    <p class="intro">Exhibit is the perfect theme for businesses, big and small, to exhibit their work. Acting as both a portfolio and a blog, Exhibit lets you show off your projects and keep your customers informed.</p>

    <h2>Flexible</h2>

    <p>While Exhibit was designed for artists and photographers to showcase their work, it’s equally suitable for bloggers, scrapbookers, and writers who want to add a bit of visual flair to their site. Plus, we designed Exhibit to look good on all devices — from desktop computers and laptops, to smartphones and tablets.</p>

    <h2>Custom Homepage Design</h2>

    <p>The default homepage layout for Exhibit is a grid of blog posts, but it also includes some custom page templates, one of which is perfect for a business to show off their latest projects. The <strong>Projects and Testimonials Homepage</strong> layout displays a grid of projects, followed by two testimonials (displayed at random).</p>

    <h2>Theme Options</h2>

    <p>Exhibit has a variety of options to let you tweak the theme so it works exactly as you want. All options work using the Customizer, so you can preview the changes as you make them, safe in the knowledge that your users won’t see a broken website.</p>

    <h2>Custom Headers and Backgrounds</h2>

    <p>Exhibit lets you upload your own header image via the Customizer’s Header Image section. You can also customize your site with a background color or image in the Colors &amp; Backgrounds area.</p>

    <h2>Widget Areas</h2>

    <p>Exhibit uses a series of custom sidebars to keep things flexible. All widget areas will stay hidden unless you add widgets to them.</p>

    <ul>
        <li><strong>Footer widget area</strong> — displays in the footer of single posts and pages, but not the homepage or archive pages.</li>
        <li><strong>Right sidebar widget area</strong> — displays on single posts and pages, but not the homepage or archive pages.</li>
    </ul>

    <h2>Testimonials</h2>

    <p>Testimonials from happy customers and clients are important to include on your site to establish trust and encourage potential customers. With Exhibit, you can add testimonials easily by going to Testimonials → Add New in your WP Admin menu. You can also add an optional customer image or logo as a featured image.</p>

    <p>Two randomly chosen testimonials appear on the front page so that every testimonial has an equal chance to be seen. All testimonials are displayed in a testimonial archive page. Once you have published a testimonial, you will be able to use the Customizer to edit the page title, add an intro text, and a featured image.</p>

    <p class="note">Note: Testimonials require the Jetpack plugin to be installed and activated</p>

    <h2>Custom Templates</h2>

    <p>Exhibit includes two custom page templates, providing additional functionality.</p>

    <ul>
        <li><strong>Projects and Testimonials Homepage</strong> — A collection of recent projects, plus some testimonial samples. Perfect for showcasing your business as a custom homepage.</li>
        <li><strong>Contributors</strong> — A list of all of the contributors to your website, sorted based on how many posts each person has published. Great for showing off the team that powers your business.</li>
        <li><strong>Full-Width Page</strong> — Displays a normal page, but with the sidebars removed so that you can have content that stretches the full width of the site.</li>
    </ul>

