    <p class="intro">Chronicle is a magazine theme. With three optional widget areas, featured posts, and a huge homepage slider there are lots of options for creating interesting, immersive websites — perfect for sites with lots of categories and content.</p>

    <p>Chronicle is a child theme of Broadsheet. This means that when you buy Chronicle you also get Broadsheet thrown in for free. Both themes are required to be uploaded to your WordPress site for Chronicle to work.</p>

    <h2>Flexible</h2>

    <p>While Chronicle was designed to power magazine and newspaper sites, it’s equally suitable for bloggers, scrapbookers, and writers who want to add a bit of visual flair to their site. Featuring a fully customizable homepage, that allows you to have four, three, two, or one columns, you really can customize what is displayed, making your site look the way you want it to.</p>

    <p>Chronicle is also very visual — using featured content and a massive homepage slider, you really can bring your biggest stories to the front.</p>

    <h2>Your Design Everywhere</h2>

    <p>We designed Chronicle to look good on all devices — from desktop computers and laptops to Android, to smartphones and tablets.</p>

    <h2>Infinite Content</h2>

    <p>Chronicle supports the all the latest WordPress wizardry, including infinite scroll. Infinite scroll helps to engage your readers by allowing them to see more of your content at once. If you have enabled footer widgets, then users will instead be given the option to load more posts.</p>

    <h2>Featured Content</h2>

    <p>Chronicle includes support for Featured Content, allowing you to showcase the biggest stories on your site. The 4 most recent posts tagged as featured content will be displayed in a prominent row of images on the homepage.</p>

    <p>To enable the slider simply select the post tag you want to use and enter it on the Appearance → Customize → Featured Content page in the Featured Content area. Then, just tag up the posts you want to be flagged as special and Chronicle will add them automatically. As you can see in the screenshot above, the slider looks great when it has a Featured Image selected.</p>

    <h2>Homepage Slider</h2>

    <p>As well as the featured content you can enable a homepage slider. This uses the most recent posts from the specified category and can be enabled through the Customizer (Appearance → Customize → Options). The slider images are full width and make a lot of impact on the homepage.</p>

    <h2>All Languages</h2>

    <p>Chronicle was crafted with care to work well in multiple languages, so it has full support for both left-to-right and right-to-left languages and is fully localized. If Chronicle is not available in your language, you can find out how to translate it here.</p>

    <h2>Custom Headers and Backgrounds</h2>

    <p>Chronicle gives you the ability to upload your own header images via Appearance → Header. You can also customize your blog with a background color or image by going to Appearance → Background.</p>

    <h2>Widget Areas</h2>

    <p>Chronicle uses a series of custom sidebars to allow you to keep things flexible. All widget areas will stay invisible if you don’t add any widgets to them.</p>

    <ul>
        <li><strong>Footer widget area</strong> – displays at the bottom of every page on the site.</li>
        <li><strong>Right sidebar widget area</strong> — will display on single posts and pages, but not the homepage or archive pages.</li>
    </ul>

    <h2>Testimonials</h2>

    <p>Testimonials from happy customers and clients are important to include on your site to establish trust and encourage potential customers. With Chronicle, you can add testimonials easily by going to Testimonials → Add New. You can also add an optional customer image or logo as a featured image.</p>

    <p>Two randomly chosen testimonials appear on the front page so that every testimonial has an equal chance to be seen. All testimonials are displayed in a testimonial archive page. Once you have published a testimonial, you can use the Customizer to edit the page title, add an intro text, and add a featured image.</p>

    <h2>Custom Templates</h2>

    <p>Chronicle includes two custom page templates. These give you some additional functionality that you wouldn’t get with a normal theme.</p>

    <ul>
        <li><strong>Contributors</strong> — A list of all of the contributors to your website (great for magazines and other multi-author blogs), sorted accorded to how many posts have been published.</li>
        <li><strong>Full-Width Page</strong> — A normal page with the sidebars removed so you can have content that stretches the full width of the site.</li>
    </ul>

