    <p class="intro">Puzzle is a visually oriented theme, great for photographers and artists who want to tell stories using their images. With a unique homepage layout, Puzzle is a creative way to show off your thoughts.</p>

    <h2>Flexible</h2>

    <p>While Puzzle was designed to allow artists and photographers to showcase their work, it’s equally suitable for bloggers, scrapbookers, and writers who want to add a bit of visual flair to their site.</p>

    <h2>Homepage Layout</h2>

    <p>Puzzle's most unique feature is the homepage layout — however, if the Puzzle layout isn't for you, then you can select from two other options — a brick-structured layout, or a more traditional grid.</p>

    <h2>Your Design, Everywhere</h2>

    <p>We designed Puzzle to look good on all devices — from desktop computers and laptops to smartphones and tablets.</p>

    <h2>Infinite Content</h2>

    <p>Puzzle supports all the latest WordPress wizardry, including Infinite Scroll (this requires the Jetpack plugin). Infinite scroll helps to engage your readers by allowing them to see more of your content at once. If you have enabled footer widgets, then users will be given the option to load more posts.</p>

    <h2>All Languages</h2>

    <p>Puzzle was crafted with care to work well in multiple languages, so it has full support for both left-to-right and right-to-left languages.</p>

    <h2>Theme Options</h2>

    <p>Puzzle has a variety of options to allow you to tweak and adjust the theme so that it works exactly as you want. All options work using the Customizer, which means you can preview the changes as you make them, safe in the knowledge that your users won't see a broken website.</p>

    <h2>Custom Headers and Backgrounds</h2>

    <p>Puzzle gives you the ability to upload your own header images via Appearance → Header. You can also customize your blog with a background color or image by going to Appearance → Background.</p>

    <h2>Social Links</h2>

    <p>Puzzle allows you to display links to your social media profiles, like Twitter and Facebook, with icons. This requires setting up a custom menu with links to your social profiles - and assigning it to the Social Menu position.</p>

    <h2>Widget Areas</h2>

    <p>Puzzle uses a series of custom sidebars to allow you to keep things flexible. All widget areas will stay invisible if you don't add widgets to them.</p>

    <ul>
        <li><strong>Footer widget area</strong> — displays at the bottom of every page on the site.</li>
        <li><strong>Right sidebar widget area</strong> — will display on single posts and pages, but not the homepage or archive pages.</li>
    </ul>

    <h2>Custom Templates</h2>

    <p>Puzzle includes some custom page templates, giving your site additional functionality.</p>

    <ul>
        <li><strong>Contributors</strong> — a list of all of the contributors to your website (great for magazines and other multi author blogs), sorted according to how many posts have been published.</li>
        <li><strong>Full-Width Page</strong> — a normal WordPress page without sidebars so that your content can stretch the full width of the site.</li>
    </ul>

