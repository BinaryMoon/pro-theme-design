    <p class="intro">Broadsheet is a newspaper theme. With 3 optional widget areas, and a huge homepage slider there are lots of options for creating interesting, immersive websites.</p>

    <h2>Flexible</h2>

    <p>While Broadsheet was designed for newspaper style sites, it’s equally suitable for bloggers – with the widget sidebars that automatically hide when they are empty – you can make your Broadsheet powered site look however you want it to.</p>

    <h2>Your Design Everywhere</h2>

    <p>We designed Broadsheet to look good on all devices – from desktop computers and laptops to Android, to smartphones and tablets.</p>

    <h2>Featured Content Slider</h2>

    <p>The Homepage Image Slider is generated from a category specified in the Customizer options (in the Theme section).</p>

    <h2>All Languages</h2>

    <p>Broadsheet was crafted with care to work well in multiple languages, so it has full support for both left-to-right and right-to-left languages and is fully localised. If Broadsheet is not available in your language, you can find out how to translate it here.</p>

    <h2>Theme Options</h2>

    <p>Broadsheet has a variety of options to allow you to tweak and adjust the theme so that it works exactly as you want. All options work using the WordPress theme customizer which means you can preview the changes as you make them safe in the knowledge that your users won’t be seeing a broken website.</p>

    <p>The Broadsheet options allow you to change the number of featured categories to display on the homepage (the thumbnail image column), whether or not to display the date and social links in the header, and what category to use for the slider.</p>

    <h2>Custom Headers and Backgrounds</h2>

    <p>Broadsheet gives you the ability to upload your own header images via Appearance → Header. You can also customize your blog with a background color or image by going to Appearance → Background.</p>

    <h2>Widget Areas</h2>

    <p>Broadsheet uses a series of custom sidebars to allow you to keep things nice and flexible. All widget areas will stay invisible if you don’t add any widgets to them.</p>

    <ul>
        <li><strong>Left sidebar widgets</strong> – are a narrow column shown on the left hand side. This column will display on the homepage, blog posts, and pages. It does not display on the archives.</li>
        <li><strong>Right sidebar widgets</strong> – are wider than the left hand widgets, and will display on all pages.</li>
        <li><strong>Footer widgets</strong> – will display on all pages and are listed under the main blog content.</li>
    </ul>

    <h2>Custom Templates</h2>

    <p>Broadsheet includes some custom page templates. These give you some additional functionality that you wouldn’t get with a normal theme.</p>

    <ul>
        <li><strong>Contributors</strong> – A list of all of the contributors to your website (perfect or newspapers and magazines with multiple authors), sorted accorded to how many posts have been published by each author.</li>
        <li><strong>Full Width Page</strong> – A normal WordPress page, only it has the sidebars removed so that you can have content that stretches the full width of the site.</li>
    </ul>

