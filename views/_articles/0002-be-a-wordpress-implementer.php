<p class="intro">WordPress Implementors are becoming increasingly common in the WordPress community. These are people who implement WordPress for small businesses. Often they will setup WordPress for their client, and then buy a theme and customise it to fit the clients requirements.</p>

<h2>The Difference Between Implementer and Developer</h2>

<p>There is a lot of confusion around naming WordPress roles. A lot of people call themselves WordPress developers when I think they would be more correctly called Implementers.</p>

<p>WordPress developers are people who create WordPress products from scratch. Whether thats themes, or plugins (or both) they are happy to work in code. They are often happy working with servers, and doing other more technical things.</p>

<p>Implementers, on the other hand, tend to be more client focused. They want to find quality themes and create happy customers.</p>

<h2>Skills Needed to be a Good Implementer</h2>

<p>To be a good WordPress Implementer you should:</p>

<ul>
    <li><strong>Have some solid HTML and CSS knowledge, and basic PHP skills</strong> - this makes it easy to customise themes, and create child themes.</li>
    <li><strong>Know how to use WordPress inside and out</strong> - knowing the WordPress admin and how to create and edit content is especially important. You may need to teach your clients this as well.</li>
    <li><strong>Be a people person</strong> - dealing with customers is probably more important than your technical skills.</li>
    <li><strong>Be good at marketing</strong> - if you can't market your services then you won't have any work.</li>
</ul>

<h2>Tips to get started as a WordPress Implementer</h2>

<p></p>

<ul>
    <li><strong>Have a preferred web host</strong> - preferably one with an affiliate program. Learn how to set up WordPress on this host. Get your clients to sign up to this (through your affiliate link) and you can get up and running quickly without having to learn a new system.</li>
    <li><strong>Have a contract ready</strong> - create a good contract that protects you should something go wrong. Define payment terms, who owns the content, and get out clauses. It's worth asking for money up front too. 50% on start of work and 50% on completion is common.</li>
    <li><strong>Have a website</strong> - somewhere to promote yourself, and show off your portfolio of clients.</li>
    <li><strong>Have a collection of 'go to' themes that you know well</strong> - make sure you know a few flxible themes inside out that you can customise easily. This will save you time and effort in the long run.</li>
    <li><strong>Prepare some commonly used plugins</strong> - like the themes, prepare a pack of plugins that cover commonly requested features that you can quickly install and use for your clients.</li>
    <li><strong>Get updatee processes in place</strong> - using a service like ManageWP can save you a lot of time and effort when managing multiple WordPress clients.</li>
</ul>

<h2>The Best of Both Worlds?</h2>

<p>There's no reason you can't cross into both worlds. I'm sure there are developers out there, who also purchase themes for clients with smaller budgets. This will save the developer time, and still give their client a quality product.</p>

<p>It works the other way too - Implementers are likely to pick up more and more PHP and technical knowledge as they go and may end up branching out in products.</p>
