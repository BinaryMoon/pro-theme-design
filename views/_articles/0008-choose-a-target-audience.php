

 - How does choosing a target audience benefit me

It makes it easier to design products

Easier to market products and services

For example when I am designing a new theme I will decide on a niche for the theme. I will then research websites that are in that niche. From that research I will think about the objectives of the people in that niche - and what they want to do. This helps select what custom features are needed.

