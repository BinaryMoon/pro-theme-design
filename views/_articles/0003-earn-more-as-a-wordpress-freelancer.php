<h2>Charge for value, not time.</h2>

<p>Charing a single fee for each project can help greatly. It can be hard for clients to take since, understandably, many like to know where their money is going. However it should be beneficial to both parties.</p>

<p>One of the problems with billing hourly for clients is that you, as the person being hired, have no incentive to work quickly. You will be paid more, the longer the job takes. This often means that clients ask for time sheets. You then have to tell clients what you've been doing, and sometimes argue over why things take the specified amount of time.</p>

<p>This can be solved by asking for a single fee. If you create a contract that outlines what you will do for the fee - then the client will know exactly how much it will cost, and you will know exactly what is required.</p>

<p>From your perspective - getting a lump sum means that you now have an incentive to create the work quickly. Since the quicker you complete the work the greater your hourly rate. It may even incentivise you to buy themes/ plugins that will speed up your work. Spending $100 to save you 10 hours work can seem a lot more appealing when you know you will have less work to do.</p>

<h2>Create Relationships</h2>

<p>Creating relationships is a good skill to have for any WordPress freelancer. Treat your clients well and they will keep coming back to you, and recommending you to their friends and colleagues.</p>

<h2>Say 'No' More</h2>

<p>Learn to say 'no'. It's hard to say no to paying work - but there's a number of different types of people who will be hard to work with. They don't want to pay much - and their work invariably takes longer than you thought - as they pile on the 'small changes'.</p>

<p>Some pointers for spotting problem clients:</p>

<ul>
    <li><strong>They haggle on the price.</strong> Some haggling is ok - but if they are trying to halve your quote they could be trouble.</li>
    <li><strong>Lack of respect.</strong> You're a professional doing a professional job. If they don't listen to you, and try to impose their ways, it's likely they don't respect your skills or time - and so will take advantage.</li>
    <li><strong>Short timeframe.</strong> If they want the website 'yesterday' it's likely that they have poor planning skills, and want to do everything quickly and cheaply.</li>
</ul>

