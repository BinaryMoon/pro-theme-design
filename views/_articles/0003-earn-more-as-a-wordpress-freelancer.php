<h2>Focus on a Target Audience</h2>

<p>When you're starting out it's tempting to accept every job that comes your way. Making sites, big and small, for whoever asks. It's hard to turn people down when you don't have much work. However - it's a good idea to pick a target audience and focus on that audience strongly.</p>

<p>So what do I mean by a target audience? I mean to specialise. Spend a bit of time working out what you are most interested in and then focus on that.</p>

<p>Don't try to be the best 'freelance WordPress Developer' be the best 'freelance WordPress Developer for comic book fans' (or other niche interest). It's a smaller audience but a lot easier to target a small focused group than targetting everyone.</p>

<p>Once you've done a few jobs for the selected target audience then you can start to know where your audience hang out. What they want to do. How they like to work. Eventually you'll have a process, and a suite of plugins and themes that do exactly what your customers want.</p>

<p>Knowing your target audience well will also make advertising easier. You'll be able to focus on the places this audience hang out. It will mean you can learn their pain points and optimise your website accordingly. Different audiences need different things - and if you can become the 'go to' person for your specific niche then people will start recommending you. Eventually you'll own the area.</p>

<p>Being specific is very important. Pick a tight focused niche. The broader the niche - the harder it will be to dominate. This may slow things down initially - but in the long term it should pay off massively.</p>
