<p class="intro">When you switch themes you may find that your images are the wrong size. This happens because each theme defines different sizes for the thumbnails.</p>

<p>Generally themes will continue to work fine when the images are the wrong size, however they may not look their best. As you add new content the older content will get pushed out and be less prominent, but it's still nice to have everything looking shiny.</p>

<p>So what we really want is to resize the images. There's all sorts of plugins that do this, but I have had some success with <a href="https://wordpress.org/plugins/regenerate-thumbnails/">Regenerate Thumbnails</a>.</p>

<p>Once you install and activate the plugin you should go to the Regenerate Thumbnails page found in the tools menu. I would suggest ticking the 'Skip regenerating existing correctly sized thumbnails (faster)', and then pressing the 'Regenerate Thumbnails for Featured Images Only'.</p>

<p>Once you have pressed the regenerate button you will need to wait for the page to finish processing the images. This could take a while (depending how many images you have), so just leave the page open and work in a different tab or make a cup of tea.</p>

<p>Once the resizing is complete test your site. Hopefully it's looking a lot better.</p>

<p>If there are still broken images then try pressing the 'Regenerate All Attachments' button. As long as you have the 'Skip correct size' option ticked, you won't have to regenerate the images you resized earlier.</p>

<p>Once that's complete all of your images will be available in the sizes required by the theme and any plugins you have.</p>

