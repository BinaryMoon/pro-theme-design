<p class="intro">Having a website is an incredibly powerful thing - and can mean you will get visitors from all around the world. However - there's no guarantee that the visitors will your native language. This can mean you are closing yourself off to some locations.</p>

<p>The best way to fix this is to publish your content in multiple languages. However this is expensive - especially if you want to translate to many languages.</p>

<p>Another solution is to create a 'translations' page, and then make use of Googles translation tools to do the translations for you. This has the down side that the translations won't be perfect - but they will be a lot better than if you were to do this manually, and best of all are free.</p>

<p>The secret lies in the fact you can create a link to a Google translation of a website - so all you have to do is create a page that links to all of the possible translations.</p>

<p>To make this easier I have created a '<a href="<?php echo path( 'tools/translation-page-generator/' ); ?>">Translations Page Generator</a>', that creates the html you need to add to your website.</p>

<p>To use this you should:</p>

<ol>
    <li>In the WordPress admin create a new page</li>
    <li>Switch the page to html mode using the tabs in the top right</li>
    <li>Generate the html for your site from the <a href="<?php echo path( 'tools/translation-page-generator/' ); ?>">translation page generator</a> - then copy and paste the code into your translation page.</li>
    <li>You can now switch back to the visual editor and tweak the page content</li>
    <li>Publish the page, then add it to your menu/ sidebar.</li>
</ol>

<p class="tip note">This process works equally well on both WordPress.com and WordPress.org hosted websites.</p>
