<p class="intro">The early versions of WordPress were simple and barebones. Users could instantly get to grips with them due to the small numbers of options available to tinker with. As the WordPress software became more complex, the WordPress team realised that it was becoming increasingly daunting for new users, and so they streamlined a number of WordPress’ core features to ensure that these new users could easily get to grips with them.</p>

<p>One of the first things novice WordPress users will need to learn is the difference between ‘posts’ and ‘pages’. While they may seem similar on the surface, there are some key differences between the two. This article will explain the difference between posts and pages, explaining the circumstances in which they should be used.</p>

<h2>TL;DR</h2>

<ul>
    <li><strong>Posts</strong> – chronological, frequently updated content often used for news and personal updates</li>
    <li><strong>Pages</strong> - static pages, generally linked to your navigation. Users for things like about pages, contact forms, and sitemaps.</li>
</ul>

<h2>Posts</h2>

<p>A blog is an ever evolving website.</p>

<p>Generally the front page of a blog will show a number of articles written by the author of the blog. Visitors will be able to click on the title of these articles to read the article in its entirety, and they will also be able to post comments and interact with the author in the comments section found at the end of the article.</p>

<p>These types of articles are known as <em>blog posts</em>. Blog posts are articles written in the <em>post</em> format.</p>

<p>With most themes the front page of a blog is updated whenever a new blog post is written. The posts are then shown in reverse chronological order; the newest posts are shown first,.</p>

<h2>Pages</h2>

<p><em>Pages</em> are static pieces of content that do not change often. They are not updated on the front page of a blog and they do not feature a date. Whereas the position of blog posts is time sensitive, blog pages will stay around forever, easily reachable through links found on the blog home page.</p>

<h2>When to use Posts and Pages</h2>

<p>Pages should be used for important content that will remain static and will always be relevant. Since pages are not cycled and replaced like posts, they should be used for content that blog visitors will want to read every time they visit.</p>

<p>‘About us’ pages, ‘contact us’ pages, ‘privacy policy’ pages, and other pages of that sort would best fit the ‘page’ format. Other important content such as ‘reports’ can be posted as a page, too.</p>

<p class="note tip">If you would like to add a contact page to your site then you can use the <a href="<?php echo path( 'documentation/feature/contact-form/' ); ?>">Contact Form</a> functionality from the <a href="<?php echo path( 'documentation/plugin/jetpack/' ); ?>">Jetpack plugin</a>.</p>

<p>Posts should be used for everything else. Since they are replaced often, posts should contain content that is not timeless and won’t be required to be read by all visitors to the blog.</p>

<p>Posts will make up the majority of content on a WordPress blog, while pages will only be used for important content that will remain on the blog for a considerable period of time.</p>

<h2>Anything Else?</h2>

<p>Blog posts and pages are the most common types of content, but WordPress is powerful enough to allow other content types as well. These can be added through plugins.</p>

<p>Many of our themes support the <a href="<?php echo path( 'documentation/feature/portfolio/' ); ?>">Portfolio</a> and <a href="<?php echo path( 'documentation/feature/testimonials/' ); ?>">Testimonials</a> post types that can be found in the <a href="<?php echo path( 'documentation/plugin/jetpack/' ); ?>">Jetpack plugin</a>.</p>

<h2>Conclusion</h2>

<p>Learning the difference between posts and pages is important. While they may appear similar at a glance, they each have their own situational uses. Pages should be used for timeless content that will remain relatively unchanged for long periods of times, while posts should be used for content that will eventually be replaced by newer blog posts on the front page of the blog. Make sure to experiment with both posts and pages – they are both useful in their own ways.</p>
