<p class="intro">As of the May 25th 2018 the General Data Protection Regulation, known as the GDPR, has come into force. This is a big update to privacy laws for European residents. This document outlines some of the GDPR factors you need to take into account when using a Pro Theme Design theme.</p>

<p class="note">Please note that this is not legal advice. This document should be used as guidance only and, if required, you should seek help from a legal professional to ensure your website is GDPR compliant.</p>

<h2>Privacy Policy</h2>

<p>As of WordPress 4.9.6 WordPress includes the functionality to create a privacy policy page on your website. This can be done in <em>Admin -> Settings -> Privacy</em>. If you have set up this page then our themes will automatically display a privacy policy link in your websites footer.</p>

<p>If you do not see the privacy policy link then please make sure you have the latest version of the theme installed.</p>

<h2>User Data Processing</h2>

<p>Pro Theme Design themes do not store or process data about website visitors. Our themes are designed purely to address the design of your website.</p>

<p>Some administrator data (email, and license key) may be sent to our servers. This is purely for theme license verification purposes. Please see our <a href="<?php echo path( '/privacy-policy/' ); ?>">privacy policy</a> for more on this.</p>

<h2>Google Fonts</h2>

<p>All of our themes support Google fonts. You can see the <a href="https://developers.google.com/fonts/faq">Google Fonts privacy policy here</a>.</p>

<p>When a request is made to Googles servers to load the fonts some data is transferred. You can list this in your privacy policy, or you can store the Google fonts locally on your own server - this requires some work on your part and is not something we can help with currently.</p>

<h2>Jetpack Plugin</h2>

<p>The Jetpack plugin is developed and maintained by Automattic. You can see the <a href="https://jetpack.com/support/privacy/">Jetpack privacy policy here</a>.</p>

<p>To use Jetpack you must connect your site with wordpress.com; this requires an account on their site. The purpose of this connection is so that services like website stats will work - however this will mean wordpress.com is storing your users data.</p>

<p>To disable this transfer of data you can either disable the features you're not using from the Jetpack admin. Or you can install the <a href="https://wordpress.org/plugins/jetpack-dev-mode/" target="_blank" rel="noopener">Jetpack Dev Mode</a> plugin. This will disable the features that need the wordpress.com connection - whilst allowing you to use the other functionality unimpeded.</p>

