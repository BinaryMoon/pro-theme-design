<p class="intro">There are a wide variety of server and browser setups - and we try to ensure our products work as well as possible. Below are a list of the requirements for using our themes.</p>

<h2>WordPress</h2>

<p>We often make use of the latest WordPress functionality and so only support the latest version of WordPress. As such you should always update WordPress to the latest version before updating/ installing our themes.</p>

<p class="note">Note: It's always recommended that you keep WordPress up to date since the latest version will always contain security updates - ensuring your site remains protected.</p>

<h2>Server Software</h2>

<p>Our themes run great with the default WordPress requirements.</p>

<ul>
    <li>PHP 5.6 or greater</li>
    <li>MySQL 5.5 or greater</li>
    <li>The mod_rewrite Apache module</li>
</ul>

<p class="note">Note that the WordPress requirements do change from time to time, so it's worth checking the <a href="https://wordpress.org/about/requirements/">official WordPress requirements page</a>.</p>

<h2>Web Browser</h2>

<p>We use the latest web technologies to ensure our themes are as future proof as possible. This does mean some functionality won't work properly in older browsers - however we make sure the content will be visible in all browsers, it just may not look exactly the same.</p>

<p>Fully supported web browsers include:</p>

<ul>
    <li>Internet Explorer 9+</li>
    <li>Firefox 30+</li>
    <li>Chrome 30+</li>
    <li>Safari 7+</li>
    <li>Opera 12+</li>
</ul>

<p>For the best experience, and security we recommend you keep your browser up to date. Check our <a href="https://browsehappy.com/">Browse Happy</a> for a list of the most up to date web browsers.</p>
