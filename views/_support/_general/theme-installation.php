<p class="intro">After a successful theme purchase you will receive an email including your individual theme or theme bundle download link. With this link you can download the zip file that contains your theme files.</p>

<p class="note tip">On Mac OSX zip files get extracted by default - so check your downloads folder for the zip file before continuing.</p>

<h2>Installation on WordPress.com</h2>

<p>Themes purchased on WordPress.com do not need to be installed. As soon as you purchase as theme it will be available to use through the theme selector in your websites admin area.</p>

<h2>Installation via the WordPress admin panel</h2>

<p>After downloading the .zip file to your computer, please go to Appearance → Themes in your WordPress admin panel and click on Add New/ Upload Theme. Now you can choose to upload the .zip file and click the Install now button. The theme will now be uploaded as your active theme.</p>

<h2>Installation via FTP</h2>

<p>Alternatively you can also install the theme directly to your server with a FTP client (e.g. Filezilla for Windows or Cyberduck for Mac). Just drop the un-zipped theme folder into your …/wp-content/themes/ folder. You can then head over to the WordPress Admin and see your installed theme in the Appearance → Themes admin screen. From there you can activate your theme by clicking on the Activate link.</p>
