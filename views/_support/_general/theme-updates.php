<p class="intro">Our themes have the ability to auto update through the WordPress admin dashboard.</p>

<p>To take advantage of the auto update functionality you will need a <a href="<?php echo path( 'documentation/general/theme-license-key' ); ?>">theme license key</a>.</p>

<h2>Setup Auto Updates</h2>

<p>
	Automatic updates are the simplest for your site to use the latest version of the theme.
</p>

<p>
	Setting it up is relatively easy. Once your theme is active visit the <em>Appearance &rarr; Getting Started</em> page and enter your license key in the license box.
</p>

<p>
	If you don't enter a license key then your theme will continue to function - however you will not get access to theme updates. These updates can be bug fixes, security fixes, or new features.
</p>

<h2>Manual Updates through the WordPress admin</h2>

<p>
	To manually update a theme you need to delete the current version of the theme, and then reinstall it following the instructions on the <a href="<?php echo path( '/documentation/general/theme-installation/' ); ?>">theme installation documentation</a>.
</p>
