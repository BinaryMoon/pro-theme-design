<p class="intro"><strong>WordPress</strong> is a publishing platform that makes it easy for anyone to publish online. It comes in two variations: the fully hosted <a href="<?php echo WORDPRESS_COM; ?>">WordPress.com</a>, and the self-hosted version available at <a href="<?php echo WORDPRESS_ORG; ?>">WordPress.org</a>.</p>

<p>Pro Theme Design has themes available for both platforms - and you need to select the version most appropriate for your needs.</p>

<h2>WordPress.com</h2>

<p><a href="<?php echo WORDPRESS_COM; ?>">WordPress.com</a> is perfect for less technical users who just want to create a website - and have someone else take care of all the technical mumbo jumbo.</p>

<p>WordPress.com features a lot of advanced functionality that is not available on a self hosted (WordPress.org) installation of WordPress.</p>

<p>WordPress.com is updated regularly with new functionality and bug fixes.</p>

<p>If you want to use a premium theme for WordPress.com then you have two options:</p>

<ol>
	<li>You can use a free account, and buy a theme for a one off payment.</li>
	<li>You can use a business account (which has a yearly fee) and then get access to any of the premium themes, and switch themes whenever you like.</li>
</ol>

<h2>WordPress.org - self hosted</h2>

<p><a href="<?php echo WORDPRESS_ORG; ?>">WordPress.org</a> installs require web hosting and a domain name to be purchased.</p>

<p>WordPress.org installs need someone to keep track of software updates and make sure their websites are working.</p>

<p>Out of the box WordPress.org has less functionality than WordPress.com. Some of this can be added back with the <a href="<?php echo path( 'wordpress-plugins/' ); ?>">Styleguide plugin, and the Jetpack plugin</a>.</p>

