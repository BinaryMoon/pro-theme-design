<p class="intro">After purchasing Chronicle you will receive an email including your theme bundle download link. With this link you can download the zip file that contains your theme files.</p>

<p class="note">Please note that on Mac OSX zip files get extracted by default - so check your downloads folder for the zip file before continuing.</p>

<p>Child themes are different to regular WordPress themes. They rely on a <em>'parent'</em> theme to function, in this case that theme is Broadsheet. If the parent theme is not installed then the child theme will not work.</p>

<p>When you buy Chronicle you will also get access to Broadsheet, there is no need to buy both themes separately.</p>

<h2>Preparing Your Themes</h2>

<p>After downloading the .zip file to your computer you will need to extract it. Inside are two more zip files - one for the parent theme, and one for the child theme. These will need to be installed individually. The order that you install them does not matter - however Chronicle can not be activated until both themes have been installed.</p>

<h2>Installation via the WordPress admin panel</h2>

<p>This will work for self hosted sites and WordPress.com business plan. Installing your own theme is not supported on wordpress.com plans below the business one.</p>

<ol>
	<li>Go to <em>Appearance → Themes</em> in your WordPress admin panel and click on <em>Add New/ Upload Theme</em>.</li>
	<li>Now you can upload the .zip file and click the <em>Install now</em> button. You will need to do the same for both themes.</li>
	<li>Once both themes are added go to <em>Appearance &rarr; Themes</em>, and select the theme you want to activate from the list. Then press <em>activate</em></li>
</ol>

<h2>Installation via FTP</h2>

<p>Alternatively you can also install the theme directly on your server with a FTP client (e.g. Filezilla for Windows or Cyberduck for Mac). To do this you will need to extract all zip files and then drop the un-zipped theme folders into your …/wp-content/themes/ folder. You can then head over to the WordPress Admin and see your installed theme in the Appearance → Themes admin screen. From there you can activate your theme by clicking on the Activate link.</p>

<p>Once installed the themes should use the following directory structure.</p>

<ul>
	<li><code>wp-content/themes/broadsheet/...</code></li>
	<li><code>wp-content/themes/chronicle/...</code></li>
</ul>
