<p class="intro">Since we're a small team (just two of us - of which only one does this full time) we are unable to help you modify or customize your theme outside of the supported features.</p>

<p>Working out what is covered by support, and what counts as a customization can be difficult so we created this document to outline the difference between support and customization of your purchased theme.</p>

<h2>Support</h2>

<p><a href="<?php echo path( 'documentation/theme/' ); ?>">All themes have extensive documentation</a> - and if you think there's anything missing then let us know and we'll improve the docs. We're happy to help you get theme features working and make your website resemble the theme demo site.</p>

<h3>Example Support Requests</h3>

<ul>
    <li>Why doesn't the infinite scroll work like in the theme demo?</li>
    <li>My theme is broken and I don't know what has happened. Can you help please?</li>
    <li>I've found a bug in the theme that is preventing me from using the theme as I'd like.</li>
</ul>

<h2>Customization</h2>

<p>Customizing a theme involves any changes to the theme that are not mentioned on the theme info page or the support documentation. These generally involve either using plugins to change the theme functionality, or editing theme files to add features we had not originally invisioned.</p>

<p>Unfortunately we are not able to help with this however we have partnered with Codeable to offer our users <a href="<?php echo path( 'theme-customization/' ); ?>">a Theme Customization service</a>.</p>

<h3>Example Customization Requests</h3>

<ul>
    <li>How do I add an extra sidebar to this page?</li>
    <li>I want to change the look of the homepage so that it is different to the demo - what should I do?</li>
    <li>How do I add support for this new thing that?</li>
    <li>Can you recommend a plugin to improve my website?</li>
</ul>

<h2>3rd Party Plugins</h2>

<p>Our themes are coded using WordPress best practices so they should work with the majority of plugins. However there may be conflicts with some plugins since many of them work in weird and wonderful ways - and supporting all of the thousands of plugins available would be a technical nightmare.</p>

<p>The plugins we officially support, and will help resolve issues with, are listed on our <a href="<?php echo path( 'plugins/' ); ?>">plugins page</a>.</p>

