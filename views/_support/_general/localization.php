All themes support

Black Walnut is ready for theme trans­la­tions. You can add your own trans­la­tion or cust­o­mize an exis­ting trans­la­tion easily using the WordPress plugin Codestyling Localization. You can also find a detailed plugin description on the plugin homepage. (It would be great if you like to make your Black Walnut theme translation available to other theme users. Please let us know, if you want to share your translation.)
