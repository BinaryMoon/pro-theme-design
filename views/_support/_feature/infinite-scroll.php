<p class="intro">Infinite Scroll is a theme feature that continuosly loads new posts for your visitors to read. This happens on the home page and archive pages. Itâ€™s designed to help visitors read through many posts without effort.</p>

<p>As you get to the bottom of the page you'll see a loading icon display before the next set of posts load.</p>

<h2>Footers</h2>

<p>When Infinite scroll is enabled, you will see a basic footer containing your blog title, which readers can click to scroll back to the top.</p>

<p>If you have widgets in the footer then the theme will display a 'load more' button rather than automatically loading new posts.</p>

<h2>Enabling Infinite Scroll</h2>

<p>Infinite Scroll is an optional feature of the Jetpack plugin, and as such the plugin needs to be installed and activated.</p>

<p>Once activated go to the Jetpack settings page and look for the infinite scroll section - then click 'enable'.</p>
