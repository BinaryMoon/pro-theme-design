<p class="intro">With the Styleguide plugin installed, Custom Colours and Fonts can be managed through WordPress Customizer.</p>

<p>Once the <a href="https://wordpress.org/plugins/styleguide/" target="_blank" rel="noopener">Styleguide plugin</a> is installed and activated you can access the Custom Fonts and Colours through the Customizer &rarr; Colours & Fonts menu.</p>

<h2>Custom Fonts</h2>

<p>Changing fonts on your site is a very simple way to make a big difference to how your site looks. It's a great way to add some personality with little effort.</p>

<p>All of our themes support editing fonts with the Styleguide plugin.</p>

<h2>Custom Colours</h2>

<p>All themes support some basic colour editing features (background colour, and site heading colour). Some support further colour adjustments through the Styleguide plugin. Currently Puzzle is the only theme that supports custom colours fully - but we plan to add support for all themes eventually.</p>
