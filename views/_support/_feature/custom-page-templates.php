<p class="intro">Custom Page Templates are custom designs for WordPress pages. Many of our themes support Custom Pages Templates.</p>

<p>To setup a new page with a Custom Page Template you should:</p>

<ol>
    <li>Go to the WordPress admin and Add a new page or Edit and existing one.</li>
    <li>Go to the 'Page Attributes' box.</li>
    <li>Select your chosen page template from the dropdown box.</li>
    <li>Press the Publish or Update button and then view your newly styled page.</li>
</ol>

