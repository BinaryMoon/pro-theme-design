<p class="intro">Custom Page Templates are custom designs for WordPress pages. Many of our themes support Custom Pages Templates.</p>

<p>To setup a new page with a Custom Page Template you should:</p>

<ol>
	<li>Go to the WordPress admin and Add a new page or Edit and existing one.</li>
	<li>Go to the 'Page Attributes' box.</li>
	<li>Select your chosen page template from the dropdown box.</li>
	<li>Press the Publish or Update button and then view your newly styled page.</li>
</ol>

<h2>Custom Homepage</h2>

<p>Creating a custom front page is an easy way to personalise your site with little effort. Read the <a href="<?php echo path( 'documentation/feature/custom-front-page/' ); ?>">Custom Front Page documentation</a> for more info on how to do this.</p>

<h2>Custom Page Templates</h2>

<p>If you want to add your own custom page templates then you should follow the process outlined on the <a href="https://developer.wordpress.org/themes/template-files-section/page-template-files/">WordPress Custom Page Template documentation</a>. I would recommend using <a href="https://developer.wordpress.org/themes/advanced-topics/child-themes/#how-to-create-a-child-theme">a child theme</a> to make these chages. If you don't then the changes will be removed when you perform an automatic theme update.</p>
