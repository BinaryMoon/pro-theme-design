<p class="">Sticky posts are a way to make a chosen blog post stick to the front of a website. This is an easy way to bring attention to a blog post. The post will remain at the top of the site until you remove the stickiness.</p>

<p>To make a blog post Sticky you should:</p>

<ol>
    <li>Edit the post you want to make Sticky.</li>
    <li>In the Publish box, beside the post content, click the Visibility Edit link.</li>
    <li>Tick the box labelled 'Stick this post to the front page'.</li>
    <li>Press the Update button.</li>
</ol>

<p>Some themes will display the Sticky post in a slightly different way - possibly making it stand our more than the other posts.</p>
