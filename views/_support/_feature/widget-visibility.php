<p class="intro">Jetpack includes a very clever addon that allows you to show and hide plugins based upon a variety of rules that you define.</p>

<p>To make use of the Widget Visibility plugin you should:</p>

<ol>
    <li>Ensure Widget Visibility is enabled in the Jetpack Settings</li>
    <li>Go to the Customizer and open up the widget you wish to modify</li>
    <li>Click the 'visibility' button to open up the visibility properties</li>
    <li>Add the rules you want to use to show and hide the widgets. I recommend keeping these rules as simple as possible - too many and it can get very confusing!</li>
    <li>Press Save</li>
</ol>

<p>For more info check out the <a href="http://jetpack.me/support/widget-visibility/" target="_blank" rel="noopener">widget visibility</a> docs on the official Jetpack website.</p>
