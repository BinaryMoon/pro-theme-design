<p class="intro"><strong>Search Engine Optimization</strong> is a very important part of every website. If a website can't be found on Google, Bing, or any other search engine - then it might as well not exist. As such we work hard to make sure our WordPress themes are optimized for search engines.</p>

<p>The following document explains what we do to ensure your theme performs as well as possible in search engines.</p>

<h2>1. Super Speedy</h2>

<p>Our themes are designed to perform as fast as possible. Google says that speed is a ranking factor. The benefit of a fast site is that users on slower connections (such as mobile devices) can access your content quicker.</p>

<p>To keep the themes, and so your site, fast we avoid using complex database queries that will slow down your site. We also minimize the use of images (many of our themes don't use any!), and keep the number of CSS and JS files to a minimum. These all combine to allow your site to load quickly.</p>

<h2>2. Mobile Ready</h2>

<p>Tablets and phone users are now the largest internet user base so having a webite that works on a mobile device is incredibly important. Google considers says that having a mobile site is a ranking factor.</p>

<h2>3. Follow WordPress Standards</h2>

<p>Using standard WordPress functions, rather than creating custom things, allows us to do a couple of things.</p>

<ol>
    <li>Using WordPress functions means that SEO plugins will work perfectly with our themes. There's no need to worry about conflicts.</li>
    <li>Using core functions means that the queries are fast, and may well be improved as WordPress improves.</li>
    <li>This makes it easier for WordPress caching plugins to 'cache' the site content, which also makes things faster.</li>
</ol>

<h2>4. Simple HTML (and minimal javascript)</h2>

<p>Keeping your sites code simple means that search engines can more easily read your site. The less code there is - the higher the content density which means more content for search engines to index.</p>
