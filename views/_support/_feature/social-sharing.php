<p class="intro">Getting found online is difficult - so adding some Social Sharing buttons is an easy way to encourage your readers to talk about your website.</p>

<h2>Enabling Sharing</h2>

<p>Social Sharing requires the Jetpack plugin to be installed and Activated. Once activated go to the Jetpack settings and enable the Sharing module. You can then go to the Sharing settings which can be found on the WP Admin &rarr; Settings &rarr; Sharing menu.</p>

<p>On the social sharing settings page you can select what channels you are prompting people to share on. You should limit this to the channels you are active in, so that you can see when people talk about you and join in the converations.</p>

<p>You can also select the types of pages that the buttons appear on. Blog posts are the most common but sometimes you may want to show them on pages, or projects.</p>
