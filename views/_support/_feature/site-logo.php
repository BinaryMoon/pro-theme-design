<p class="intro">The Site Logo is a perfect method for adding some branding to your website. Just upload an image, and it will display in the appropriate place in your theme.</p>

<h2>Enabling the Site Logo</h2>

<p>Site Logo functionality requires the Jetpack plugin. Just install and enable the plugin and you will be able to upload a logo through the Customizer.</p>

<h2>Adding your Site Logo</h2>

<ol>
    <li>Go to the Customizer and click on the Site Title & Logo section.</li>
    <li>Click the Add Logo button to open the Media Manager.</li>
    <li>Upload a new image, or select one thatâ€™s already in your Media Library.</li>
    <li>Click Set as logo, and you will see your logo appear in the preview window.</li>
    <li>Click the Save button at the bottom right of the Customizer to activate the logo on your site.</li>
</ol>

<h2>Hiding the Site Title and Description</h2>

<p>Sometimes if you have a Site Logo then you won't need the Site Title and Description. If that's the case then above the Site Logo selection tools in the Customizer, you will see a checkbox labelled Display Header Text. To hide your site title and description, leaving just the logo, uncheck this box. Be sure to click Save to make it active on your site.</p>
