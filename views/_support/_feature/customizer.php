<p class="intro">The Customizer allows you to edit site settings whilst previewing the changes. A number of WordPress and Theme features are available to be edited. Some of the ones we support are listed below under the 'Related Features' heading.</p>

<p>To access the Customizer you can either:</p>

<ol>
    <li>Whilst viewing the actual website - click on the 'Customizer' button in the admin bar at the top of the page.</li>
    <li>When in the WordPress Admin, you can access the Customizer through the <em>Appearance &rarr; Customizer</em> menu option.</li>
</ol>
