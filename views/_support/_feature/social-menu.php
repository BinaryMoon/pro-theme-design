<p class="intro">A social links menu is a way to set up a menu that links to your social media profiles, like Twitter and Facebook, with icons.</p>

<p>To do this you should:</p>

<ol>
    <li>Create a new Menu.</li>
    <li>Add your social links to the new menu as custom links.</li>
    <li>Add it to the “Social Menu” position.</li>
</ol>

<h2>Available icons</h2>

<p>Linking to any of the following sites will automatically display their icon in your menu.</p>

