<p class="intro"><a href="<?php echo JETPACK; ?>">Jetpack</a> includes a simple Contact Form manager. This module allows you to easily add and edit contact forms on any post or page.</p>

<p>To add a form you should:</p>

<ol>
	<li>Ensure Contact Forms is enabled in the Jetpack Settings</li>
	<li>Edit the page you want to add the form to/ Create a new page</li>
	<li>Click the button above the content editor labelled 'Add Contact Form'</li>
	<li>There will be some default fiels, but you can delete/ add new ones on the screen that appears</li>
	<li>Go to the advanced tab to set a subject line and define where the email will be sent.</li>
	<li>Press 'Add this form to my post'</li>
	<li>Update/ Publish the post</li>
</ol>

<p>For more info check out the <a href="https://jetpack.me/support/contact-form/">contact form docs</a> on the official Jetpack website.</p>
