<p class="intro">The <a href="<?php echo JETPACK; ?>">Jetpack plugin</a> includes a module called Photon. This is a special module that takes care of image resizing, and cropping, always serving the best image possible.</p>

<h2>Image Resizing</h2>

<p>So why do you need this? When you upload an image to WordPress it automatically creates the thumbnails needed for the current theme. However if you then change theme you will find that your thumbnails are the wrong size. Photon takes care of this and resizes all of your old images.</p>

<h2>Image Serving</h2>

<p>Another advantage of Photon is that it serves as a CDN (Content Delivery Network). A CDN distributes multiple copies of your images to many servers all around the world. Then, when someone requests an image, it serves the image from the closest server. Not only does this make the image serving faster - but it means that your own server doesn't have to do any image serving. So it saves you money too!</p>
