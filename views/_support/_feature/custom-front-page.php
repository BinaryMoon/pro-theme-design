<p class="intro">Custom Front Pages, are static pages that display content you have designed. These can use normal WordPress pages, or WordPress pages with <a href="<?php echo path( 'documentation/feature/custom-page-templates/' ); ?>">Custom Page Templates.</a></p>

<p>To use a page as your Front Page you should:</p>

<ol>
    <li>Create a new, empty page (eg with the title of 'Front Page').</li>
    <li>Optionally you can select the Custom Pate Template you want to use for the Front Page layout.</li>
    <li>If using a normal Page then you should fill our the page content as desired.</li>
    <li>Pulish your new page.</li>
    <li>Go to the Customizer &rarr; Static Front Page.</li>
    <li>Select your new Front page as the front page option.</li>
    <li>You can also select a second page that will be used as the url for your blog posts.</li>
</ol>
