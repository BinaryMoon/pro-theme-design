<h2>Theme Demo</h2>

<p>The Exhibit theme demo uses a Custom Front Page to display the projects and testimonials on the homepage. To duplicate this you should:</p>

<ol>
    <li>Create a new page and assign the projects template to it (using a <a href="<?php echo path( 'documentation/feature/custom-page-templates/' ); ?>">Custom Page Templates</a>).</li>
    <li>Assign the newly created page as a <a href="<?php echo path( 'documentation/feature/custom-front-page/' ); ?>">Custom Front Page</a>.</li>
</ol>

<h2>Sidebars</h2>

<p>Exhibit has 2 optional sidebars. They will only display if they contain widgets.</p>

<ul>
    <li><strong>Sidebar Widgets</strong> - this displays on the right hand side on single blog posts and pages.</li>
    <li><strong>Footer Widgets</strong> - this displays in the footer and uses javascript to intelligently position the widgets to make sure they slot together nicely.</li>
</ul>

<h2>Custom Templates</h2>

<p>Exhibit has 3 <a href="<?php echo path( 'documentation/feature/custom-page-templates/' ); ?>">Custom Page Templates</a>. These can be used as different layouts for your websites pages, or as a <a href="<?php echo path( 'documentation/feature/custom-front-page/' ); ?>">Custom Front Page</a>.</p>

<ul>
    <li><strong>Full Width</strong> - this lets you display your page content full width, with no sidebar.</li>
    <li><strong>Contributors</strong> - this page displays a list of all the people who have published content on your blog.</li>
    <li><strong>Projects</strong> - this page displays a list of all the <a href="<?php echo path( 'documentation/feature/portfolio/' ); ?>">projects</a> you have published.</li>
</ul>
