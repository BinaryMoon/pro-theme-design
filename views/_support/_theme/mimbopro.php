<h2>Homepage Layout</h2>

<img src="<?php echo image_path( 'docs/layout-mimbopro.png' ); ?>" alt="Mimbo Pro Homepage Layout" class="align" />

<ol>
	<li><strong>Lead Featured Category</strong> The featured category is controlled via a property set in the Theme Options in the WordPress Admin.</li>
	<li><strong>Carousel</strong> Posts selected from the Carousel Categories options in the Theme Options admin page will display in a rotating carousel here.</li>
	<li><strong>Sidebar Left Widgets</strong> Add widgets to the widget stack called 'Sidebar Left' and they will be displayed here. Leave the widgets empty and the remaining content will scale to fit.</li>
	<li><strong>Featured Categories</strong> Displays a list of categories and their most recent posts. Posts will only be shown once in the featured category list so if they are in multiple categories then they will be shown the first time and not subsequent times. This maximises the number of different posts shown on the homepage. You can select the categories in the Theme Options page in the admin.</li>
	<li><strong>Sidebar Right Widgets</strong> Add widgets to the widget stack called 'Sidebar Right' and they will be displayed here. Leave the widgets empty and the remaining content will scale to fit.</li>
</ol>


<h2>Theme Options</h2>

<p>Mimbo Pro has a number of theme settings that can be controlled through the settings page found in the <em>WordPress Admin &rarr; Appearance &rarr; Theme Options</em> menu page.</p>

<ul>
	<li><strong>Lead / Featured Category</strong> - the first post from the category selected here will appear in the highlighted box above the slider on the homepage.</li>
	<li><strong>Carousel Categories</strong> - the categories used here will be placed in the scolling carousel on the homepage.</li>
	<li><strong>Carousel Scroll Speed</strong> - 0 will auto scroll the carousel. Any other number will be the number of seconds to pause between carousel position changes.</li>
	<li><strong>Category Summaries</strong> - The categories selected, and arranged, here will be displayed on the homepage in the center column.</li>
</ul>

