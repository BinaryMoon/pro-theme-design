<h2>Custom Templates</h2>

<p>Mirror has 2 <a href="<?php echo path( 'documentation/feature/custom-page-templates/' ); ?>">Custom Page Templates</a>. These can be used as different layouts for your websites pages, or as a <a href="<?php echo path( 'documentation/feature/custom-front-page/' ); ?>">Custom Front Page</a>.</p>

<ul>
    <li><strong>Full Width</strong> - this lets you display your page content full width, with no sidebar.</li>
    <li><strong>Contributors</strong> - this page displays a list of all the people who have published content on your blog.</li>
</ul>
